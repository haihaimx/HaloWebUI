import { IMAGES_API_BASE_URL } from '$lib/constants';

export type ImageGenerationRequest = {
	prompt: string;
	model?: string;
	size?: string;
	n?: number;
	negative_prompt?: string;
	credential_source?: 'auto' | 'personal' | 'shared';
	connection_index?: number;
	steps?: number;
	background?: string;
};

export type ImageUsageConfig = {
	enabled: boolean;
	engine: 'openai' | 'gemini' | 'comfyui' | 'automatic1111' | string;
	defaults: {
		model?: string;
		size?: string;
		steps?: number;
	};
	shared_key: {
		enabled: boolean;
		available: boolean;
	};
	personal_key: {
		supported: boolean;
		provider: 'openai' | 'gemini' | null | string;
	};
};

export const getConfig = async (token: string = '') => {
	let error = null;

	const res = await fetch(`${IMAGES_API_BASE_URL}/config`, {
		method: 'GET',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			...(token && { authorization: `Bearer ${token}` })
		}
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			console.log(err);
			if ('detail' in err) {
				error = err.detail;
			} else {
				error = 'Server connection failed';
			}
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

